# Usando diversos tipos de dados - Calculadora

# Calculadora

number1 = int(input("Digite o 1 numero: "))
number2 = int(input("Digite o 2 numero: "))

sum = number1 + number2 #soma
subtraction = number1 - number2 #subtração
division = number1 / number2 #divisão
multiplication = number1 * number2 #multiplicação

print(f"Soma= {sum} \nSubtração= {subtraction} \nDivisão= {division} \nMultiplicação= {multiplication}")